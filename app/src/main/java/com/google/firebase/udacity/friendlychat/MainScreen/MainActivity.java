package com.google.firebase.udacity.friendlychat.MainScreen;

import android.os.Bundle;
import android.support.v4.app.FragmentManager;
import android.support.v7.app.AppCompatActivity;
import android.view.Menu;
import android.view.MenuInflater;
import android.view.MenuItem;

import com.google.firebase.udacity.friendlychat.R;
import com.google.firebase.udacity.friendlychat.util.ActivityUtil;

public class MainActivity extends AppCompatActivity {

    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_main);

        FragmentManager fragmentManager = getSupportFragmentManager();
        MainScreenFragment dashboardFragment = (MainScreenFragment) fragmentManager.findFragmentById(R.id.fragmentContainer);
        if (dashboardFragment == null) {
            dashboardFragment = MainScreenFragment.newInstance();
            ActivityUtil.addFragmentToActivity(fragmentManager, dashboardFragment, R.id.fragmentContainer);
        }
    }

    @Override
    public boolean onCreateOptionsMenu(Menu menu) {
        MenuInflater inflater = getMenuInflater();
        inflater.inflate(R.menu.main_menu, menu);
        return true;
    }

    @Override
    public boolean onOptionsItemSelected(MenuItem item) {
        return super.onOptionsItemSelected(item);
    }

    @Override
    protected void onDestroy() {
        super.onDestroy();
    }
}
